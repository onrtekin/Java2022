package day18_nestedForLoop;

public class C06_WhileLoop {

    public static void main(String[] args) {


        int baslangic=40;
        int bitis=60;


        for (int i = baslangic; i <=bitis ; i++) {
            System.out.print(i+" ");

        }
        System.out.println("");


        while (baslangic<=bitis){
            System.out.print(baslangic+" ");
            baslangic++;

        }







    }


}
