package day18_nestedForLoop;

public class C14 {
    public static void main(String[] args) {
        int baslangic=40;
        int bitis=60;
        for (int i = baslangic; i <=bitis ; i++) {
            System.out.print(i+" ");

        }
        System.out.println("");

int i=baslangic;

        while (i<=bitis){
            System.out.print(i+" ");
            i++;
        }


    }
}
